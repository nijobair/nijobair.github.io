-- Solution:
SELECT full_name
FROM students
WHERE english = math;





/*
This SQL query finds the students whose English score is equal to their Math score.
1. `SELECT full_name` retrieves the full names of the students.
2. `FROM students` specifies the table from which to retrieve the data.
3. `WHERE english = math` filters the results to include only students whose English score is the same as their Math score.
In summary, this query provides the names of students who have the same score in both English and Math.
*/